#ifndef _STD_DATA_TYPES_H
#define _STD_DATA_TYPES_H


typedef unsigned char                            uint_t8;
typedef volatile unsigned char                   vuint_t8;
typedef char                                     sint_t8;
typedef volatile char                            vsint_t8;
typedef unsigned short int                       uint_t16;
typedef volatile unsigned short int              vuint_t16;
typedef short int                                sint_t16;
typedef volatile short int                       vsint_t16;
typedef unsigned int                             uint_t32;
typedef volatile unsigned int                    vuint_t32;
typedef int                                      sint_t32;
typedef volatile int                             vsint_t32;
typedef unsigned long long int                   uint_t64;
typedef volatile unsigned long long int          vuint_t64;
typedef long long int                            sint_t64;
typedef volatile long long int                   vsint_t64;
typedef float                                    f_t32;
typedef volatile float                           vf_t32;
typedef double                                   f_t64;
typedef volatile double                          vf_t64;



#endif